import {Elemental} from "elemental/elemental";

export default Elemental("antiScrape", elemental => {
    let webdriver = atob('d2ViZHJpdmVy'),
        _phantom = atob('X3BoYW50b20='),
        callPhantom = atob('Y2FsbFBoYW50b20=');

    if (window.navigator[webdriver] === true ||
        window.document.documentElement.getAttribute(webdriver) ||
        window[callPhantom] ||
        window[_phantom]
    )
        window.location = atob("aHR0cHM6Ly9nb29nbGUuY29t");
    elemental.el.parentNode.removeChild(elemental.el);
})
